/*******************************************************************************
*@brief Calculo do polinomio de grua n
*******************************************************************************
*@param x - Valor de x.
*@return Retorna o valor pol(x).
*******************************************************************************
*@date      2021 - 2021
*@author    Henrique C. C. de Andrade
*******************************************************************************/
double Polinomio::polinomio(double const x) {

  double tmp;

  tmp = a[0];
  for (int i = 0; i < this->nTerm; i++) {
    tmp += this->a[i] * pow(x, i);
  }

  return tmp;
}
/******************************************************************************/

/*******************************************************************************
*@details Seta os coeficientes
*******************************************************************************
*@param a     - Coeficientes do polinomio.
*@param nTerm - Numero de termos no polinomio.
*******************************************************************************
*@date      2021 - 2021
*@author    Henrique C. C. de Andrade
*******************************************************************************/
void Polinomio::set_a(const double* const a, short nTerm) {
  this->set_nTerm(nTerm);
  this->set_a(a);
}
/******************************************************************************/

/*******************************************************************************
*@details Seta os coeficientes
*******************************************************************************
*@param a     - Coeficientes do polinomio.
*******************************************************************************
*@date      2021 - 2021
*@author    Henrique C. C. de Andrade
*******************************************************************************/
void Polinomio::set_a(const double* const a) {
  for (int i = 0; i < this->nTerm; i++) {
    this->a[i] = a[i];
  }
}
/******************************************************************************/